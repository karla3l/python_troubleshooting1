# Little John fez uma viagem dos EUA para o Brasil. Porém, chegando aqui, descobriu que a
# temperatura é medida em graus Celsius, e não em Fahrenheit. Dada a temperatura em graus Celsius,
# qual a temperatura correspondente em graus Fahrenheit? Use a formula
# f=c*(9/5)+32

c = int(input('Digite a temperatura em graus Celsius: \t'))
f = c*(9//5)+32
print('A temperatura em graus Fahrenheit é: \t', f)